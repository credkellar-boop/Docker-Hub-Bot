import os
import subprocess
import google.generativeai as genai

# Configure the Brain Module
genai.configure(api_key=os.environ.get("GEMINI_API_KEY"))
model = genai.GenerativeModel('gemini-1.5-pro')

class DockerHubBot:
    def __init__(self):
        # Target repos to unify the docker-library ecosystem
        self.library_repos = [
            "docker-library-bot",
            "docker-library/official-images",
            "docker-library/docs",
            "docker-library/repo-info",
            "docker-library-transitioner",
            "docker-library/faq"
        ]

    def bootstrap_assets(self):
        """Asset Module: Synchronizes the core docker-library repositories."""
        print("[*] Initializing Asset Module...")
        for repo_path in self.library_repos:
            repo_name = repo_path.split("/")[-1]
            if not os.path.exists(repo_name):
                # Handles both standalone users and the docker-library org
                org = "docker-library" if "/" not in repo_path else repo_path.split("/")[0]
                url = f"https://github.com/{org}/{repo_name}.git"
                subprocess.run(["git", "clone", url])

    def secure_environment(self, image_name):
        """Security Module: Automates vulnerability scanning."""
        print(f"[*] Security Module auditing {image_name}...")
        subprocess.run(["docker", "scout", "quickview", image_name])

    def brain_search_and_install(self, query):
        """Brain Module: Gemini searches, validates, and provisions environments."""
        print(f"[*] Brain Module analyzing Docker Hub for: {query}")
        
        prompt = f"""
        You are a systems architecture bot. Search for the optimal official Docker image for '{query}'.
        Generate a robust configuration including:
        1. A 'Dockerfile' utilizing strict layer caching optimizations.
        2. A '.dockerignore' to prevent unnecessary payload bloat.
        3. A 'docker-compose.yml' file to securely orchestrate the environment.
        Output ONLY the raw file contents wrapped in tags like ---DOCKERFILE---, ---COMPOSE---, etc.
        """
        
        response = model.generate_content(prompt)
        print("[*] Configuration Generated by Gemini. Provisioning...")
        
        # Save the unified output generated by the AI
        with open("unified_infrastructure.yaml", "w") as f:
            f.write(response.text)
            
        print("[*] Spinning up multi-container environment...")
        # subprocess.run(["docker", "compose", "up", "-d"])

if __name__ == "__main__":
    # Initialize Docker-Hub-Bot
    bot = DockerHubBot()
    bot.bootstrap_assets()
    
    # Example execution
    bot.brain_search_and_install("high-throughput rust development environment")
